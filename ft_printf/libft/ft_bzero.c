#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = (char *)(s);
	while (n)
	{
		ptr[i] = '\0';
		i++;
		n--;
	}
}
